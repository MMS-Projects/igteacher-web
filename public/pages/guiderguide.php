    <h2>7. Tips & Trucks</h2>
    Als guider sta je constant in contact met de leerlingen. Denk eraan dat je zelf ook ooit een noob was. En dat een leerling niet zoveel weet als jij. Hier zijn enkele tips & trucks om een team te leiden en spelers te leren spelen. Deze tips zijn gewoon tips, het zijn geen richtlijnen, en je moet ze niet volgen.
    <ul>
        <li>Wees er zeker van dat alle mentoren dezelfde informatie aan de spelers geven. Het is stom wanneer 2 mentoren verschillende informatie geven. Dus je moet dikwijls communiceren over wat je tegen de leerlingen zegt. Je kan hier ook best enkele richtlijnen over maken.</li>
        <li>Probeer altijd kalm te blijven. Als je nerveus lijkt naar mentoren zullen zij dat ook zijn.</li>
        <li>Gedraag je al een leider. Je kan grappen maken, maar wees zeker dat mentoren je zien als een leider!</li>
        <li>Hou je aan je woord. Maak geen beloftes die je niet kan houden.</li>
        <li>Wees eerlijk naar alle leerlingen. Een goede leider heeft geen favorieten.</li>
        <li>Wees een goede communicator. Je hebt geen commanderede stem nodig om een goede guider te zijn, maar je moet naar voor durven stappen en zeggen 'Kom op!'. Een goede guider weet hoe hij informatie moet geven en krijgen, zodat iedereen weet wat er aan de hand is.</li>
        <li>Wees flexibel. Niet alles gaat altijd volgens plan. Wees voorbereid om naar een plan B te gaan wanneer A niet gewerkt heeft.</li>
        <li>Wees georganiseerd. De tijd die je spendeerd aan het plannen zal beloond worden met veel vrije tijd. Stel enkele mensen aan die bereid zijn om wat verantwoordelijkheid op zich te nemen.</li>
        <li>Besteed enkele taken uit. Sommige mensen denken dat ze alles beter kunnen, zonder dat ze het niet zelf gedaan hebben. De meeste mentoren houden ervan als ze een uitdaging krijgen. Stel je team voor een uitdaging door ze iets te laten doen dat ze nog nooit gedaan hebben.</li>
        <li>Wees een voorbeeld. Het best wat je kan doen als leider is een voorbeel zijn voor je team. Wat je ook doet, sommige mensen zullen je na doen. Een blije attitude kan de teamgeest hoog houden.</li>
        <li>Wees consiquent. Niks is meer frustrerend dan een guider die het ene moment zo is en dan het andere moment helemaal anders is. Als mensen weten wat ze van jou kunnen verwachten, dan zullen ze meestal positief naar je opkijken.</li>
        <li>Geen complimentjes. Als je je team gemotiveerd wilt houden, is meestal eens zeggen 'Goed gedaan!' al voldoende.</li>
        <li>Vraag om hulp. Wees zeker niet beschmaamd om hulp te vragen. Wanneer je geconfronteerd wordt met een situatie en je weet niet hoe je deze moet behandelen, vraag iemand met meer ervaring om raad of om hulp. Je kan altijd op het management rekenen voor hulp.</li>
    </ul>
